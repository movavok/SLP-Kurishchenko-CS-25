def genRanSentence(adjectives, nouns, verbs):
    """
    Генерує випадкове речення з трьох частин: прикметник, іменник, дієслово.
    """
    import random
    print(f"{random.choice(adjectives)} {random.choice(nouns)} {random.choice(verbs)}")




